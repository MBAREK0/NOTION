package com.MBAREK0.web.entity;

import jakarta.persistence.*;

/**

 *     id SERIAL PRIMARY KEY,
 *     username VARCHAR(50) NOT NULL UNIQUE,
 *     password VARCHAR(255) NOT NULL,
 *     first_name VARCHAR(50) NOT NULL,
 *     last_name VARCHAR(50) NOT NULL,
 *     email VARCHAR(100) NOT NULL UNIQUE,
 *     role VARCHAR(20) NOT NULL CHECK (role IN ('manager', 'user'))

 */

@Entity
@Table(name = "users")
public class User {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false, unique = true)
    private String username;

    @Column(nullable = false)
    private String password;

    @Column(name = "first_name", nullable = false)
    private String firstName;

    @Column(name = "last_name", nullable = false)
    private String lastName;

    @Column(nullable = false, unique = true)
    private String email;

    @Enumerated(EnumType.STRING)
    private UserOrManager role;

    public User() {
    }

    public User(String username, String password, String firstName, String lastName, String email, UserOrManager role) {
        this.username = username;
        this.password = password;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.role = role;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public UserOrManager getRole() {
        return role;
    }

    public void setRole(UserOrManager role) {
        this.role = role;
    }

    @Override
    public String toString() {
        return "User{" +
                ", username='" + username + '\'' +
                ", password='" + password + '\'' +
                ", firstName='" + firstName + '\'' +
                ", lastName='" + lastName + '\'' +
                ", email='" + email + '\'' +
                ", role='" + role + '\'' +
                '}';
    }
}

